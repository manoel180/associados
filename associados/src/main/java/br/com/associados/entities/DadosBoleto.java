package br.com.associados.entities;

public class DadosBoleto {

	private String nomePagador;
	
	private String nomeBeneficiario;
	
	private String agencia;
	
	private String digitoAgencia;
	
	private String codigoBeneficiario;
	
	private String digitoCodigoBeneficiario;
	
	private String numeroConvenio;
	
	private String carteira;
	
	private String localPagamento;

	/**
	 * @return the nomePagador
	 */
	public String getNomePagador() {
		return nomePagador;
	}

	/**
	 * @param nomePagador the nomePagador to set
	 */
	public void setNomePagador(String nomePagador) {
		this.nomePagador = nomePagador;
	}

	/**
	 * @return the nomeBeneficiario
	 */
	public String getNomeBeneficiario() {
		return nomeBeneficiario;
	}

	/**
	 * @param nomeBeneficiario the nomeBeneficiario to set
	 */
	public void setNomeBeneficiario(String nomeBeneficiario) {
		this.nomeBeneficiario = nomeBeneficiario;
	}

	/**
	 * @return the agencia
	 */
	public String getAgencia() {
		return agencia;
	}

	/**
	 * @param agencia the agencia to set
	 */
	public void setAgencia(String agencia) {
		this.agencia = agencia;
	}

	/**
	 * @return the digitoAgencia
	 */
	public String getDigitoAgencia() {
		return digitoAgencia;
	}

	/**
	 * @param digitoAgencia the digitoAgencia to set
	 */
	public void setDigitoAgencia(String digitoAgencia) {
		this.digitoAgencia = digitoAgencia;
	}

	/**
	 * @return the codigoBeneficiario
	 */
	public String getCodigoBeneficiario() {
		return codigoBeneficiario;
	}

	/**
	 * @param codigoBeneficiario the codigoBeneficiario to set
	 */
	public void setCodigoBeneficiario(String codigoBeneficiario) {
		this.codigoBeneficiario = codigoBeneficiario;
	}

	/**
	 * @return the digitoCodigoBeneficiario
	 */
	public String getDigitoCodigoBeneficiario() {
		return digitoCodigoBeneficiario;
	}

	/**
	 * @param digitoCodigoBeneficiario the digitoCodigoBeneficiario to set
	 */
	public void setDigitoCodigoBeneficiario(String digitoCodigoBeneficiario) {
		this.digitoCodigoBeneficiario = digitoCodigoBeneficiario;
	}

	/**
	 * @return the numeroConvenio
	 */
	public String getNumeroConvenio() {
		return numeroConvenio;
	}

	/**
	 * @param numeroConvenio the numeroConvenio to set
	 */
	public void setNumeroConvenio(String numeroConvenio) {
		this.numeroConvenio = numeroConvenio;
	}

	/**
	 * @return the carteira
	 */
	public String getCarteira() {
		return carteira;
	}

	/**
	 * @param carteira the carteira to set
	 */
	public void setCarteira(String carteira) {
		this.carteira = carteira;
	}

	/**
	 * @return the localPagamento
	 */
	public String getLocalPagamento() {
		return localPagamento;
	}

	/**
	 * @param localPagamento the localPagamento to set
	 */
	public void setLocalPagamento(String localPagamento) {
		this.localPagamento = localPagamento;
	}
	
	
	
}
